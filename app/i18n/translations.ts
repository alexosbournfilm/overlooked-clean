function preserveBrand(value: string) {
  return value.replace(/Overlooked/g, BRAND_PLACEHOLDER);
}

function restoreBrand(value: string) {
  return value.replace(new RegExp(BRAND_PLACEHOLDER, 'g'), 'Overlooked');
}

function applySourceCase(source: string, translated: string) {
  if (source.length > 1 && source === source.toLocaleUpperCase()) {
    return translated.toLocaleUpperCase();
  }

  return translated;
}

function getDirectTranslation(source: string, language: AppLanguageCode) {
  const exact = PHRASES[source]?.[language] || CREATIVE_ROLE_PHRASES[source]?.[language];
  if (exact) return exact;

  const roleKey = Object.keys(CREATIVE_ROLE_PHRASES).find(
    (key) => key.toLocaleLowerCase() === source.toLocaleLowerCase()
  );

  if (!roleKey) return null;

  const translated = CREATIVE_ROLE_PHRASES[roleKey]?.[language];
  return translated ? applySourceCase(source, translated) : null;
}

function translateKnownDelimitedList(source: string, language: AppLanguageCode) {
  if (!/,\s*|\s+\+\s*|\s+\/\s+/.test(source)) return null;

  const parts = source.split(/(,\s*|\s+\+\s*|\s+\/\s+)/);
  let translatedAny = false;

  const translatedParts = parts.map((part) => {
    if (/^(,\s*|\s+\+\s*|\s+\/\s+)$/.test(part)) return part;

    const leading = part.match(/^\s*/)?.[0] || '';
    const trailing = part.match(/\s*$/)?.[0] || '';
    const term = part.trim();
    if (!term) return part;

    const translated = getDirectTranslation(term, language);
    if (!translated) return null;

    translatedAny = true;
    return `${leading}${translated}${trailing}`;
  });

  if (!translatedAny || translatedParts.some((part) => part == null)) return null;
  return translatedParts.join('');
}

function isNonTranslatableToken(source: string) {
  return /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(source) || /^https?:\/\//i.test(source);
}

function translateTextInternal(
  value: string,
  language: AppLanguageCode,
  options: { allowDynamicFallback?: boolean } = {}
) {
  if (language === 'en' || !value) return value;

  const protectedValue = preserveBrand(value);
  const leading = protectedValue.match(/^\s*/)?.[0] || '';
  const trailing = protectedValue.match(/\s*$/)?.[0] || '';
  const trimmed = protectedValue.trim();
  if (!trimmed) return value;

  const direct = getDirectTranslation(restoreBrand(trimmed), language) || getDirectTranslation(trimmed, language);
  if (direct) return `${leading}${restoreBrand(direct)}${trailing}`;

  const sourceText = restoreBrand(trimmed);
  if (isNonTranslatableToken(sourceText)) return value;

  const delimited = translateKnownDelimitedList(sourceText, language);
  if (delimited) return `${leading}${restoreBrand(delimited)}${trailing}`;

  const cachedDynamic = getCachedDynamicTranslation(sourceText, language);
  if (cachedDynamic) return `${leading}${restoreBrand(cachedDynamic)}${trailing}`;

  if (trimmed.startsWith('•')) {
    const bulletBody = restoreBrand(trimmed.replace(/^•\s*/, ''));
    const translatedBody = translateTextInternal(bulletBody, language, options);

    if (translatedBody !== bulletBody) {
      return `${leading}• ${restoreBrand(translatedBody)}${trailing}`;
    }
  }

  if (trimmed.includes('•')) {
    const translatedParts = trimmed
      .split('•')
      .map((part) => translateTextInternal(restoreBrand(part.trim()), language, options));
    const translatedComposite = translatedParts.join(' • ');

    if (translatedComposite !== restoreBrand(trimmed)) {
      return `${leading}${restoreBrand(translatedComposite)}${trailing}`;
    }
  }

  for (const entry of DYNAMIC_PATTERNS) {
    const match = restoreBrand(trimmed).match(entry.pattern);
    if (!match) continue;
    const translated = entry.replace(match, language);
    if (translated) return `${leading}${restoreBrand(translated)}${trailing}`;
  }

  if (options.allowDynamicFallback || isStaticUiPhrase(sourceText)) {
    requestDynamicTranslation(sourceText, language, {
      allowUnregistered: options.allowDynamicFallback,
    });
  }

  return value;
}

export function translateText(value: string, language: AppLanguageCode) {
  return translateTextInternal(value, language);
}

export function translateTrustedText(value: string, language: AppLanguageCode) {
  if (language === 'en' || !value) return value;

  if (value.includes('\n')) {
    return value
      .split(/(\n+)/)
      .map((part) =>
        /^\n+$/.test(part)
          ? part
          : translateTextInternal(part, language, { allowDynamicFallback: true })
      )
      .join('');
  }

  return translateTextInternal(value, language, { allowDynamicFallback: true });
}

export function hasTranslation(value: string, language: AppLanguageCode) {
  return translateText(value, language) !== value;
}
